# Funções
n1 = 10
n2 = 5

def somar():
    r = n1 + n2
    print(f"A soma de {n1} + {n2} = {r}")

def subtrair():
    r = n1 - n2
    print(f"A subtração de {n1} - {n2} = {r}")

def multiplica():
    r = n1 * n2
    print(f"A multiplicação de {n1} * {n2} = {r}")

def calculos():
    somar()
    subtrair()
    multiplica()

calculos()